CREATE TABLE reservation_items (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    medicine_id BIGINT,
    quantity INT,
    price DOUBLE,
    reservation_id BIGINT,
    FOREIGN KEY (medicine_id) REFERENCES medicine(id),
    FOREIGN KEY (reservation_id) REFERENCES reservations(id)
);

